#include "libft.h"

void	*ft_memset(void *b, int c, int len)
{
	char	*d;
	int		i;

	d = (char *)b;
	i = 0;
	while (i < len)
	{
		d[i] = (unsigned char)c;
		i++;
	}
	return (d);
}
